# 整型和浮点型
num1 = 1
num2 = 1.2
print(type(num1))
print(type(num2))

# 这是一个字符串类型
a = 'hello,world!'
print(type(a))

# 这是一个布尔类型
b = True
print(type(b))

# 这是一个列表类型
c = [10,20,30]
print(type(c))

# 这是一个元组类型
d = (10,20,30)
print(type(d))

# 这是一个集合
e = {10,20,30}
print(type(e))

# 这是一个字典
f = {'name': 'TOM','age': 18}
print(type(f))

# ）常见的格式符号：%s （字符串） %d（有符号的十进制整数） %f（浮点数） %c（字符） 这几种是常见的格式符号，如果需要其他的再去查就好了。